#include <iostream>

using namespace std;

int main()
{
    int a = 5, b = 4;
    cout << a << endl;

    // Phep cong
    cout << a + b << endl;

    // Phep tru
    cout << a - b << endl;

    //Phep nhan
    cout << a*b << endl;

    //Phep chia lay nguyen
    cout << a/b << endl;

    //Chia lay du
    cout << a % b << endl;

    // ep kieu 1.25 = 5.0 / 4.0
    cout << double(a)/double(b)<<endl;

    cout << int(5.4);

    return 0;
}